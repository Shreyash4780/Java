public class Q21 {
    public static void main(String[] args) {
        
        Integer intObj = 10; 
        Double doubleObj = 5.5;
        Character charObj = 'A';

        System.out.println("Integer: " + intObj);
        System.out.println("Double: " + doubleObj);
        System.out.println("Character: " + charObj);
    }
}

// Output =>
// Integer: 10
// Double: 5.5
// Character: A
